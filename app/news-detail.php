                                        <p>Produce Leader Commits to Sharing Stories from Dedicated Workforce, Thanking Essential Employees for Outstanding Work During Coronavirus Outbreak</p>
                                        <p>Coral Gables, FL (June 3, 2020) – Del Monte Fresh Produce N.A., Inc., one of North America’s leading marketers and distributors of high-quality fresh and fresh-cut fruit and vegetables, is proud to today introduce its new ‘Can Do Done Daily’ campaign, which aims to highlight its dedicated workforce, who have been going above and beyond during these trying times. The new campaign, which features stories from Del Monte Fresh Produce employees in all sectors – from warehouse operations to merchandising and more – showcases the exemplary work Del Monte Fresh Produce’s staff has remained committed to executing throughout the COVID-19 outbreak, despite the challenges they have faced.</p>
                                        <p>Del Monte Fresh Produce employees from across the continent were asked by the company to share stories highlighting their hard work amidst this new normal for the campaign. These dedicated essential workers bravely shared details on their work, shining a spotlight on the ways each member of the company has been going above and beyond as part of the Del Monte Fresh Produce family to ensure that consumers continue to receive access to fresh fruits and vegetables in their communities. From helping to stock supermarket shelves to quickly adapting to shipping and storage changes, Del Monte Fresh Produce’s workforce has displayed a tried and true commitment to freshness, quality and innovation.</p>
                                        <p>“We are so proud to share the stories collected for the Can Do Done Daily campaign with our consumers,” said Elana Gold, Chief Marketing Officer, Del Monte Fresh Produce. “So many members of the Del Monte Fresh Produce family have been working around the clock to keep our operation going; our employees have gone above and beyond and given their all, despite how challenging things have been. Showcasing their passion, commitment and the hard work they’ve been doing in this way is the least we can do to thank them for their outstanding efforts, and it is our hope that our consumers will join in on thanking them, too.”  </p> 
                                        <p>For more information on fresh products from Del Monte®, including products, recipes and promotions, please visit <a href="https://www.delmontefresh.com/" target="_blank" rel="noopener">https://www.delmontefresh.com/</a>, or keep up with the brand on Instagram or YouTube @delmontefresh.</p>
                                        <h2>About Del Monte Fresh Produce N.A., Inc.</h2>
